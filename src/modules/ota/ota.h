#pragma once

/**
 * @brief Инициализирует OTA подсистему (callbacks и runtime-состояние).
 * @details
 * - Вызывать один раз в `setup()` после `wifiInit()`.
 * - Неблокирующая.
 * - Реальный старт OTA сервера выполняется позже в `otaLoop()`, когда есть сеть.
 * - Не ISR-safe.
 */
void otaInit();

/**
 * @brief Периодическая обработка OTA.
 * @details
 * - Вызывать в каждом проходе `loop()`.
 * - При первом доступном подключении поднимает OTA сервер.
 * - Потенциально блокирующая во время активной OTA-загрузки.
 * - Не ISR-safe.
 */
void otaLoop();

/**
 * @brief Возвращает готовность OTA сервера.
 * @return true если OTA сервер уже запущен и принимает обновления.
 */
bool otaIsReady();
